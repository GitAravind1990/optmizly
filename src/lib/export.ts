type TopicalPillar = {
  title: string; slug: string; monthly_searches: string; covered: boolean; ai_cite_score: number
  clusters: Array<{ title: string; slug: string; difficulty: string; monthly_searches: string; covered: boolean; primary_keyword: string }>
}

type TopicalData = {
  topic?: string; authority_score?: number; coverage_grade?: string
  pillar_pages?: TopicalPillar[]
  coverage_stats?: { total_topics: number; covered: number; missing: number; coverage_pct: number }
  content_calendar?: Array<{ week: number; title: string; type: string; why_now: string }>
}

export function exportTopicalCSV(data: TopicalData) {
  const rows: string[][] = []
  rows.push(['TOPICAL AUTHORITY MAP'], ['Niche', data.topic ?? ''], ['Authority Score', String(data.authority_score ?? 0)], ['Coverage Grade', data.coverage_grade ?? ''], [''])
  rows.push(['PILLAR & CLUSTER MAP'], ['Type', 'Title', 'Monthly Searches', 'Covered', 'Difficulty', 'AI Cite Score', 'Primary Keyword'])
  ;(data.pillar_pages ?? []).forEach(p => {
    rows.push(['PILLAR', p.title, p.monthly_searches, p.covered ? 'Yes' : 'No', '', String(p.ai_cite_score), ''])
    ;(p.clusters ?? []).forEach(c => rows.push(['  Cluster', c.title, c.monthly_searches, c.covered ? 'Yes' : 'No', c.difficulty, '', c.primary_keyword]))
  })
  rows.push([''], ['CONTENT CALENDAR'], ['Week', 'Title', 'Type', 'Why Now'])
  ;(data.content_calendar ?? []).forEach(c => rows.push([`Week ${c.week}`, c.title, c.type, c.why_now]))
  downloadCSV('optmizly-topical-authority.csv', rows)
}

