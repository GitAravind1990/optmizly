export function exportSeoAuditCSV(data: SeoAuditData) {
  const catMap = Object.fromEntries(AUDIT_FRAMEWORK.map(c => [c.key, c.title]))
  const rows: string[][] = []

  rows.push(['ENTERPRISE SEO AUDIT REPORT'])
  rows.push(['URL', data.url])
  rows.push(['Page Title', data.pageTitle ?? ''])
  rows.push(['Date', new Date(data.createdAt).toLocaleDateString()])
  rows.push([''])

  rows.push(['OVERALL SCORE', String(data.overallScore), '/100'])
  rows.push(['Passed Checks', String(data.passedChecks), `of ${data.totalChecks}`])
  rows.push(['Warning Checks', String(data.warnChecks)])
  rows.push(['Failed Checks', String(data.failedChecks)])
  if (data.backlinkData?.oprScore != null) {
    rows.push(['Domain Authority (OPR)', data.backlinkData.oprScore.toFixed(1), '/10'])
    if (data.backlinkData.domainRank && data.backlinkData.domainRank > 0) {
      rows.push(['Global Domain Rank', `#${data.backlinkData.domainRank.toLocaleString()}`])
    }
  }
  rows.push([''])

  rows.push(['CATEGORY SCORES'])
  rows.push(['Category', 'Score', 'Priority', 'Tag'])
  Object.entries(data.categoryScores).forEach(([key, score]) => {
    const cat = AUDIT_FRAMEWORK.find(c => c.key === key)
    if (cat) rows.push([cat.title, String(score), cat.priority, cat.tag])
  })
  rows.push([''])

  rows.push(['AUTO-DETECTED ISSUES (Fails & Warnings)'])
  rows.push(['Check ID', 'Label', 'Status', 'Detail'])
  Object.entries(data.autoResults).forEach(([id, result]) => {
    if (result.status === 'fail' || result.status === 'warn') {
      const cat = AUDIT_FRAMEWORK.flatMap(c => c.subCategories.flatMap(s => s.checks)).find(c => c.id === id)
      rows.push([id, cat?.label ?? '', result.status.toUpperCase(), result.detail])
    }
  })
  rows.push([''])

  rows.push(['AI ASSESSMENTS'])
  rows.push(['Category', 'Score', 'Top Issues', 'Top Fixes'])
  Object.entries(data.aiResults).forEach(([key, result]) => {
    const cat = AUDIT_FRAMEWORK.find(c => c.key === key)
    if (cat) {
      rows.push([
        cat.title,
        String(result.score),
        (result.issues ?? []).slice(0, 2).join(' | '),
        (result.fixes ?? []).slice(0, 2).join(' | ')
      ])
    }
  })
  rows.push([''])

  rows.push(['MANUAL CHECKLIST (User-Set Items)'])
  rows.push(['Check ID', 'Label', 'Status'])
  Object.entries(data.checklistState).forEach(([id, status]) => {
    const cat = AUDIT_FRAMEWORK.flatMap(c => c.subCategories.flatMap(s => s.checks)).find(c => c.id === id)
    if (cat) rows.push([id, cat.label, status.toUpperCase()])
  })

  downloadCSV('Optmizly-seo-audit.csv', rows)
}

