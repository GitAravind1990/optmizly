type SerpData = {
  competitors?: Array<{ rank: number; url: string; authority: string; page_type: string; word_count: string; eeat: string }>
  root_causes?: Array<{ dimension: string; diagnosis: string; severity: string }>
  action_plan?: { phase1?: Array<{ task: string; why: string; effort: string; impact: string }>; phase2?: Array<{ task: string; why: string; effort: string; impact: string }>; phase3?: Array<{ task: string; why: string; effort: string; impact: string }> }
  rank_projection?: Array<{ month: string; position: number }>
  _meta?: { url: string; keyword: string; position?: string }
}

export function exportSerpCSV(data: SerpData) {
  const rows: string[][] = []
  rows.push(['SERP AUDIT REPORT'], ['Keyword', data._meta?.keyword ?? ''], ['Target URL', data._meta?.url ?? ''], [''])
  rows.push(['COMPETITORS'], ['Rank', 'URL', 'Authority', 'Page Type', 'Word Count', 'E-E-A-T'])
  ;(data.competitors ?? []).forEach(c => rows.push([String(c.rank), c.url, c.authority, c.page_type, c.word_count, c.eeat]))
  rows.push([''], ['ROOT CAUSES'], ['Dimension', 'Diagnosis', 'Severity'])
  ;(data.root_causes ?? []).forEach(rc => rows.push([rc.dimension, rc.diagnosis, rc.severity]))
  rows.push([''], ['ACTION PLAN — PHASE 1 (Weeks 1-4)'], ['Task', 'Why', 'Effort', 'Impact'])
  ;(data.action_plan?.phase1 ?? []).forEach(t => rows.push([t.task, t.why, t.effort, t.impact]))
  rows.push([''], ['ACTION PLAN — PHASE 2 (Weeks 5-10)'], ['Task', 'Why', 'Effort', 'Impact'])
  ;(data.action_plan?.phase2 ?? []).forEach(t => rows.push([t.task, t.why, t.effort, t.impact]))
  rows.push([''], ['RANK PROJECTION'], ['Month', 'Projected Position'])
  ;(data.rank_projection ?? []).forEach(r => rows.push([r.month, String(r.position)]))
  downloadCSV('optmizly-serp-audit.csv', rows)
}

